    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if(contador_editar == 0) {
            jLabel14.setForeground(Color.ORANGE);
            jLabel14.setText("Clique Novamente em Editar Para Confirmar");      
            contador_editar = 1;
        } else if(contador_editar == 1) {
            if("".equals(jTextField2.getText()) && "".equals(jFormattedTextField2.getText()) && "".equals(jFormattedTextField3.getText())) {
                jLabel14.setForeground(Color.RED);
                jLabel14.setText("Você Precisa alterar pelo menos um Dado para Editar");   
                contador_editar = 0;
            } else {
                String codigo = jTextField1.getText();
                if(!("".equals(jTextField2.getText()))) {
                    Metodos.CadastroProdutos.atualizarNomeProduto(codigo, jTextField2.getText());
                }
                if(!("".equals(jFormattedTextField2.getText()))) {
                    String sv = jFormattedTextField2.getText();
                    String vsf = sv.replace("R$", "").replace(",", ".");                    
                    Metodos.CadastroProdutos.atualizarValorProduto(codigo, Double.parseDouble(vsf));
                }
                if(!("".equals(jFormattedTextField3.getText()))) {
                    Metodos.CadastroProdutos.atualizarQuantidadeProduto(codigo, Integer.parseInt(jFormattedTextField3.getText()));
                }
                jLabel14.setForeground(Color.GREEN);
                jLabel14.setText("Produto Atualizado com Sucesso.");  
                contador_editar = 0;
            }
        }
            

    }//GEN-LAST:event_jButton6ActionPerformed

