    public void tremer() {
            Point p = this.getLocation();
            TelaGerenciarProdutos tl = this;
            new Thread() {
                @Override
                public void run() {
                    try {
                        for (int i = 0; i < 4; i++) {
                            tl.setLocation(p.x-10, p.y);
                            sleep(20);
                            tl.setLocation(p.x+10, p.y);
                            sleep(20);
                        } 
                        tl.setLocation(p.x, p.y);
                        }catch (InterruptedException ex) {
                                Logger.getLogger(TelaGerenciarProdutos.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

            }.start();
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        jLabel14.setText("");
        if(!("".equals(jTextField2.getText()) || "".equals(jFormattedTextField2.getText()) || "".equals(jTextField1.getText()) || "".equals(jFormattedTextField3.getText()))) {
            String nome_produto = jTextField2.getText();
            String sv = jFormattedTextField2.getText();
            String vsf = sv.replace("R$", "").replace(" ","").replace(",", ".");
            double valor_produto = Double.parseDouble(vsf);
            String codigo_produto = jTextField1.getText();
            int quantidade_inicial = Integer.parseInt(jFormattedTextField3.getText());            
            if(Metodos.CadastroProdutos.verificarExisteCod(codigo_produto)) {
                jLabel14.setForeground(Color.RED);
                jLabel14.setText("Esse Código Já é Cadastrado no Estoque. Representa: " + Metodos.CadastroProdutos.retornarNome(codigo_produto));
                tremer();
            }
            if(Metodos.CadastroProdutos.verificarExisteNome(nome_produto)) {
                jLabel14.setForeground(Color.RED);
                jLabel14.setText("Esse Nome de Produto Já é Cadastrado no Estoque.");
                tremer();
            }
            if(Metodos.CadastroProdutos.verificarExisteNome(nome_produto) == false && Metodos.CadastroProdutos.verificarExisteCod(codigo_produto) == false) {
                Metodos.CadastroProdutos.criarProduto(nome_produto, valor_produto, codigo_produto, quantidade_inicial);
                jLabel14.setForeground(Color.GREEN);
                jLabel14.setText("Produto Cadastrado no Estoque com Sucesso.");
            }
        } else {
            jLabel14.setForeground(Color.RED);
            jLabel14.setText("Aparentemente Faltam dados para o Cadastro, verifique os Campos.");          
            tremer();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

