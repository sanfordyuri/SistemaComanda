    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed

        if(contador_editar == 0) {
            labela.setForeground(Color.ORANGE);
            labela.setText("Clique Novamente em Editar Para Confirmar");      
            contador_editar = 1;
        } else if(contador_editar == 1) {
                if(!"".equals(jTextField1.getText())) {
                    if(Metodos.CadastroProdutos.verificarSeComandaExiste(jTextField1.getText())) {
                        Metodos.CadastroProdutos.removeC(jTextField1.getText());
                        readJTable();   
                        labela.setForeground(Color.GREEN);
                        labela.setText("Comanda Resetado com Sucesso.");  
                        contador_editar = 0;                       
                    } else {
                        tremer();
                        labela.setForeground(Color.RED);
                        labela.setText("Essa comanda nao Está em uso. Ou seja, ja está resetada.");                      
                        contador_editar = 0;                        
                    }

                } else {
                    tremer();
                    labela.setForeground(Color.RED);
                    labela.setText("Insira o Código da Comanda, para Reseta-la");                      
                    contador_editar = 0;
                } 
        }        
    }//GEN-LAST:event_jButton6ActionPerformed

