    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        if(!("".equals(jTextField1.getText()))) {
            String comanda = jTextField1.getText().toLowerCase();
                if(!("".equals(jTextField1) || "".equals(jTextField2) || "".equals(jFormattedTextField3.getText()))) {
                    if(Metodos.CadastroProdutos.verificarExisteCod(jTextField2.getText())) {
                    try {
                        Metodos.CadastroProdutos.createTableComanda(comanda);
                    } catch (ClassNotFoundException | SQLException ex) {
                        Logger.getLogger(TelaGerenciarComanda.class.getName()).log(Level.SEVERE, null, ex);
                    } 
                    String codigo = jTextField2.getText();  
                    int quantidade = Integer.parseInt(jFormattedTextField3.getText());
                    
                    if(Metodos.CadastroProdutos.retornarQuantidade(codigo) >= quantidade) {
                        Date d = new Date();
                        SimpleDateFormat sf = new SimpleDateFormat("HH:mm:ss");
                        String df = sf.format(d);
                        Metodos.CadastroProdutos.addProdutoComanda(comanda,codigo.toUpperCase(),quantidade,df);
                        readJTable();     
                        labela.setForeground(Color.GREEN);
                        labela.setText("Item Inserido na Comanda.");                         
                    } else {
                        labela.setForeground(Color.red);
                        labela.setText("Não Existe estoque suficiente para Esse produto.");                          
                    }
                             
                } else {
                    tremer();
                    labela.setForeground(Color.red);
                    labela.setText("Esse Código de Produto não Existe.");                   
                }       
                } else {
                    labela.setForeground(Color.red);
                    labela.setText("Existe campos em Branco. Verifique os Campos.");        
                    tremer();
                }
            
        }
    }//GEN-LAST:event_jButton8ActionPerformed

