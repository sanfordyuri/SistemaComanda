    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(!"".equals(jTextField1.getText())) {
            String comanda = jTextField1.getText();
            if(Metodos.CadastroProdutos.verificarSeComandaExiste(comanda)) {
               this.setVisible(false);
               TelaFecharComanda tfc = new TelaFecharComanda();
               tfc.setVisible(true);      
               if(tfc == null) {
                   tfc = new TelaFecharComanda();
                   tfc.setVisible(true);
                   tfc.recebendo(comanda);
               } else {
                   tfc.setVisible(true);
                   tfc.setState(TelaFecharComanda.NORMAL);
                   tfc.recebendo(comanda);
               }
            } else {
                labela.setForeground(Color.red);
                labela.setText("Essa Comanda nao Existe. Ou seja, nao foi aberta");
                tremer();
            }   
        } else {
            labela.setForeground(Color.red);
            labela.setText("Insira um Código de Comanda.");
            tremer();
            
        }
    }//GEN-LAST:event_jButton5ActionPerformed

